    add_p(doc, why)
    add_p(doc, "Почему это важно для Star Carpet:", bold=True, space_after=2)
    add_p(doc, important)
    add_p(doc, "Влияние на продажи / доверие:", bold=True, space_after=2)
    add_p(doc, impact)


